package com.example.root.g_track.Model;

public class Alatmodel {
    private String namaalat;
    private String key;
    private String classalat;
    private String merek;
    private String tipe;
    private int tahun;
    private String implemen;
    private int harga;
    private String deskripsi;
    private String piduser;

    public Alatmodel() {
    }


    public String getNamaalat() {
        return namaalat;
    }

    public void setNamaalat(String namaalat) {
        this.namaalat = namaalat;
    }

    public String getClassalat() {
        return classalat;
    }

    public void setClassalat(String classalat) {
        this.classalat = classalat;
    }

    public String getMerek() {
        return merek;
    }

    public void setMerek(String merek) {
        this.merek = merek;
    }

    public String getTipe() {
        return tipe;
    }

    public void setTipe(String tipe) {
        this.tipe = tipe;
    }

    public int getTahun() {
        return tahun;
    }

    public void setTahun(int tahun) {
        this.tahun = tahun;
    }

    public String getImplemen() {
        return implemen;
    }

    public void setImplemen(String implemen) {
        this.implemen = implemen;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getPiduser() {
        return piduser;
    }

    public void setPiduser(String piduser) {
        this.piduser = piduser;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
